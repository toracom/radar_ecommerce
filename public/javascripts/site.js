$('#play').on('click', function (e) {
    e.preventDefault();
    $("#player")[0].src += "?autoplay=1";
    $('#player').show();
    $('#video-cover').hide();
})

$('#modal1').on('hidden.bs.modal', function (e) {
    $('#modal1 iframe').attr("src", $("#modal1 iframe").attr("src"));
});

